print(2 + 3)   # addition(+)
print(3 - 1)   # subtraction(-)
print(2 * 3)   # multiplication(*)
print(3 / 2)   # division(/) returns integer or double value, depending on remainder
print(3 ** 2)  # exponential(**)
print(3 % 2)   # modulus(%)
print(3 // 2)  # Floor division operator(//) returns integer value

# Checking data types

print(type(10))                  # Int
print(type(3.14))                # Float
print(type(1 + 3j))              # Complex
print(type('Asabeneh'))          # String
print(type([1, 2, 3]))           # List
print(type({'name':'Asabeneh'})) # Dictionary: are used to store data values in key:value pairs (ordered, changeable, no duplicates)
print(type({9.8, 3.14, 2.7}))    # Set: are used to store multiple items in a single variable (unorder, unchangeable, and unindexed)
print(type((9.8, 3.14, 2.7)))    # Tuple: are used to store multiple items in a single variable (ordered and unchangeable)